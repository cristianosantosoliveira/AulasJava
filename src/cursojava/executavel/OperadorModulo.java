package cursojava.executavel;

public class OperadorModulo {

	public static void main(String[] args) {
		//Modulo: Resto da divisão %
		
		double carro = 9;
		double pessoas = 2;
		
		double resto = carro % pessoas;
		
		System.out.println("Sobraram (= modulo de divisão) exatamente " + resto + " carro de " + carro + " " +
		" dividido por " + pessoas + " pessoas ");

	}

}
